# Os operadores unários são aqueles que exigem apenas um operando, ou seja
#, um valor sobre o qual a operação é realizada.
# Alguns exemplos de operadores unários em Python são:

# O operador de negação lógica (not), que inverte o valor lógico do operando. 
#Se o operando for verdadeiro, o resultado será falso, e vice-versa.
a = True # declara uma variável a com valor verdadeiro
print(a) # imprime o valor de a
print(not a) # imprime o valor de not a (negação lógica)

# O operador de inversão de bits (~), que inverte todos os bits do operando. 
#O resultado é o complemento de dois do operando.
b = 5 # declara uma variável b com valor 5
print(b) # imprime o valor de b
print(~b) # imprime o valor de ~b (inversão de bits)

# O operador de sinal (+ ou -), que indica se o valor do operando é positivo 
#ou negativo. O operador + não altera o valor do operando, mas o 
#operador - muda o sinal do operando.
c = 10 # declara uma variável c com valor 10
print(c) # imprime o valor de c
print(+c) # imprime o valor de +c (sinal positivo)
print(-c) # imprime o valor de -c (sinal negativo)


