# Código em Ruby
# Em Ruby, não é necessário usar ponto-e-vírgula no final das linhas
# Em Ruby, não é necessário usar parênteses para imprimir valores com puts
# Em Ruby, as variáveis locais começam com letras minúsculas ou sublinhados (_)

x = 10 # declara uma variável x com valor 10
y = 5 # declara uma variável y com valor 5
puts "x + y = #{x + y}" # imprime o resultado da soma
puts "x - y = #{x - y}" # imprime o resultado da subtração
puts "x * y = #{x * y}" # imprime o resultado da multiplicação
puts "x / y = #{x / y}" # imprime o resultado da divisão
puts "x % y = #{x % y}" # imprime o resultado do resto
puts "x == y = #{x == y}" # imprime o resultado da comparação de igualdade
puts "x != y = #{x != y}" # imprime o resultado da comparação de diferença
puts "x > y = #{x > y}" # imprime o resultado da comparação de maior
puts "x < y = #{x < y}" # imprime o resultado da comparação de menor
puts "x >= y = #{x >= y}" # imprime o resultado da comparação de maior ou igual
puts "x <= y = #{x <= y}" # imprime o resultado da comparação de menor ou igual
puts "x && y = #{x && y}" # imprime o resultado da conjunção lógica
puts "x || y = #{x || y}" # imprime o resultado da disjunção lógica

