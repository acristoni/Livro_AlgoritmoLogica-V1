# Código em Ruby
# Em Ruby, não é necessário usar ponto-e-vírgula no final das linhas
# Em Ruby, não é necessário usar parênteses para imprimir valores com puts
# Em Ruby, as variáveis locais começam com letras minúsculas ou sublinhados (_)

x = 10 # declara uma variável x com valor 10
y = 5 # declara uma variável y com valor 5
z = x > y ? x : y # atribui o maior valor entre x e y a z usando o operador ternário
puts "z = #{z}" # imprime o valor de z usando interpolação de strings

