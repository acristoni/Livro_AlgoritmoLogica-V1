// Código em C
#include <stdio.h>

int main() {
  int x = 10; // declara uma variável x com valor 10
  int y = 5; // declara uma variável y com valor 5
  printf("x + y = %d\n", x + y); // imprime o resultado da soma
  printf("x - y = %d\n", x - y); // imprime o resultado da subtração
  printf("x * y = %d\n", x * y); // imprime o resultado da multiplicação
  printf("x / y = %d\n", x / y); // imprime o resultado da divisão
  printf("x %% y = %d\n", x % y); // imprime o resultado do resto
  printf("x == y = %d\n", x == y); // imprime o resultado da comparação de igualdade
  printf("x != y = %d\n", x != y); // imprime o resultado da comparação de diferença
  printf("x > y = %d\n", x > y); // imprime o resultado da comparação de maior
  printf("x < y = %d\n", x < y); // imprime o resultado da comparação de menor
  printf("x >= y = %d\n", x >= y); // imprime o resultado da comparação de maior ou igual
  printf("x <= y = %d\n", x <= y); // imprime o resultado da comparação de menor ou igual
  printf("x && y = %d\n", x && y); // imprime o resultado da conjunção lógica
  printf("x || y = %d\n", x || y); // imprime o resultado da disjunção lógica
  return 0;
}

