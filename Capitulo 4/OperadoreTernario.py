# Código em Python

# Declara uma variável x com valor 10
# Em Python, não é necessário usar ponto-e-vírgula no final das linhas
# Em Python, não é necessário usar parênteses para imprimir valores com print, mas é recomendado para evitar ambiguidades

x = 10 
# Declara uma variável y com valor 5
y = 5 
z = x if x > y else y # atribui o maior valor entre x e y a z usando o operador ternário
print(f"z = {z}") # imprime o valor de z usando f-strings para interpolar os valores das variáveis
