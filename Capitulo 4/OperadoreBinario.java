// Código em Java
public class OperadoresBinarios {
  public static void main(String[] args) {
    int x = 10; // declara uma variável x com valor 10
    int y = 5; // declara uma variável y com valor 5
    System.out.println("x + y = " + (x + y)); // imprime o resultado da soma
    System.out.println("x - y = " + (x - y)); // imprime o resultado da subtração
    System.out.println("x * y = " + (x * y)); // imprime o resultado da multiplicação
    System.out.println("x / y = " + (x / y)); // imprime o resultado da divisão
    System.out.println("x % y = " + (x % y)); // imprime o resultado do resto
    System.out.println("x == y = " + (x == y)); // imprime o resultado da comparação de igualdade
    System.out.println("x != y = " + (x != y)); // imprime o resultado da comparação de diferença
    System.out.println("x > y = " + (x > y)); // imprime o resultado da comparação de maior
    System.out.println("x < y = " + (x < y)); // imprime o resultado da comparação de menor
    System.out.println("x >= y = " + (x >= y)); // imprime o resultado da comparação de maior ou igual
    System.out.println("x <= y = " + (x <= y)); // imprime o resultado da comparação de menor ou igual
    System.out.println("x && y = " + (x && y)); // imprime o resultado da conjunção lógica
    System.out.println("x || y = " + (x || y)); // imprime o resultado da disjunção lógica
  }
}

