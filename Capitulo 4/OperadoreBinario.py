# Os operadores binários são aqueles que exigem dois operandos, 
#ou seja, dois valores entre os quais a operação é realizada.
# Alguns exemplos de operadores binários em Python são:

# O operador de adição (+), que soma os valores dos dois operandos.
x = 10 # declara uma variável x com valor 10
y = 5 # declara uma variável y com valor 5
z = x + y # declara uma variável z com o resultado da soma
print(f"x + y = {z}") # imprime o resultado da soma 
print(f"x - y = {z}") # imprime o resultado da subtração
print(f"x * y = {z}") # imprime o resultado da multiplicação
print(f"x / y = {z}") # imprime o resultado da divisão
print(f"x % y = {z}") # imprime o resultado do resto
print(f"x == y = {z}") # imprime o resultado da comparação de igualdade
print(f"x != y = {z}") # imprime o resultado da comparação de diferença
print(f"x > y = {z}") # imprime o resultado da comparação de maior
print(f"x < y = {z}") # imprime o resultado da comparação de menor
print(f"x >= y = {z}") # imprime o resultado da comparação de maior ou igual
print(f"x <= y = {z}") # imprime o resultado da comparação de menor ou igual
print(f"x and y = {z}") # imprime o resultado da conjunção lógica
print(f"x or y = {z}") # imprime o resultado da disjunção lógica

