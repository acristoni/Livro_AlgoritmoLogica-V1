// Código em Java
public class OperadoresUnarios {
  public static void main(String[] args) {
    int x = 5; // declara uma variável x com valor 5
    System.out.println("x = " + x); // imprime o valor de x
    System.out.println("!x = " + !x); // imprime o valor de !x (negação lógica)
    System.out.println("++x = " + ++x); // imprime o valor de ++x (incremento pré-fixado)
    System.out.println("x = " + x); // imprime o novo valor de x
    System.out.println("x++ = " + x++); // imprime o valor de x++ (incremento pós-fixado)
    System.out.println("x = " + x); // imprime o novo valor de x
    System.out.println("--x = " + --x); // imprime o valor de --x (decremento pré-fixado)
    System.out.println("x = " + x); // imprime o novo valor de x
    System.out.println("x-- = " + x--); // imprime o valor de x-- (decremento pós-fixado)
    System.out.println("x = " + x); // imprime o novo valor de x
    System.out.println("+x = " + +x); // imprime o valor de +x (sinal positivo)
    System.out.println("-x = " + -x); // imprime o valor de -x (sinal negativo)
  }
}

