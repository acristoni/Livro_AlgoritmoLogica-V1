// Declara uma variável do tipo ArrayList de String chamada nomes e atribui
// a ela uma nova lista vazia usando o operador new
ArrayList<String> nomes = new ArrayList<String>();
// Adiciona alguns elementos à lista usando o método add
nomes.add("Ana");
nomes.add("Bruno");
nomes.add("Carlos");
// Imprime na tela o valor da variável nomes
System.out.println("Os nomes são " + nomes);
// Modifica um elemento da lista usando o método set
nomes.set(1, "Bianca");
// Imprime na tela o novo valor da variável nomes
System.out.println("Os novos nomes são " + nomes);
// Remove um elemento da lista usando o método remove
nomes.remove(2);
// Imprime na tela o novo valor da variável nomes
System.out.println("Os nomes finais são " + nomes);


