// Define um tipo de struct chamado pessoa
struct pessoa {
  char nome[50]; // Um campo do tipo vetor de char que armazena o nome da pessoa
  int idade; // Um campo do tipo int que armazena a idade da pessoa em anos
  char sexo; // Um campo do tipo char que armazena o sexo da pessoa ('M' ou 'F')
  float altura; // Um campo do tipo float que armazena a altura da pessoa em metros
  float peso; // Um campo do tipo float que armazena o peso da pessoa em quilogramas
};

